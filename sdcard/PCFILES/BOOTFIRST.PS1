# C64 SD2IEC NTSC boot-first finalizer v13
# This is run by PCFILES\RUN.BAT after downloads finish.
# Goal: put FB first in the root directory creation/FAT order so LOAD"*",8 loads FileBrowser.

param(
    [string]$RootArg
)

try {
    if ($RootArg) { $Root = [System.IO.Path]::GetFullPath($RootArg) }
    else { $Root = [System.IO.Path]::GetPathRoot((Get-Location).Path) }
} catch {
    $Root = [System.IO.Path]::GetPathRoot((Get-Location).Path)
}

if (!$Root.EndsWith('\')) { $Root += '\' }

function RootPath($name) { return (Join-Path $Root $name) }

Write-Host ""
Write-Host "Boot-first finalizer v13"
Write-Host "Card root: $Root"

$fbPath = RootPath 'FB'
if (!(Test-Path -LiteralPath $fbPath)) {
    Write-Host 'FB was not found at the root. Skipping boot-first reorder.' -ForegroundColor Yellow
    exit 0
}

# Use a root hold folder. Windows/FAT normally reuses deleted root entries, so moving
# existing entries aside and then moving FB back first makes FB the first match for LOAD"*",8.
$holdName = '__HOLD'
$hold = RootPath $holdName
$i = 0
while (Test-Path -LiteralPath $hold) {
    $i++
    $holdName = ('__HOLD{0}' -f $i)
    $hold = RootPath $holdName
}
New-Item -ItemType Directory -Force -Path $hold | Out-Null

# Only touch files/folders generated by this kit. Do not touch Windows system folders.
$knownDirs = @('TOOLS','DEMOS','DISKMAG','GAMES','GAMETEST','PUBLIC','BASIC','MUSIC','PCFILES')
$knownFiles = @('README.TXT','LOADS.TXT')

# Include all FB* variants at the root.
$fbItems = Get-ChildItem -LiteralPath $Root -Force -File -ErrorAction SilentlyContinue | Where-Object { $_.Name -match '^FB' }

$itemsToMove = @()
$itemsToMove += $fbItems
foreach ($d in $knownDirs) {
    $p = RootPath $d
    if (Test-Path -LiteralPath $p) { $itemsToMove += Get-Item -LiteralPath $p -Force }
}
foreach ($f in $knownFiles) {
    $p = RootPath $f
    if (Test-Path -LiteralPath $p) { $itemsToMove += Get-Item -LiteralPath $p -Force }
}

# Deduplicate by full path and avoid the hold folder itself.
$seen = @{}
$itemsToMove = $itemsToMove | Where-Object {
    $_.FullName -ne $hold -and !$seen.ContainsKey($_.FullName) -and ($seen[$_.FullName] = $true)
}

foreach ($item in $itemsToMove) {
    try {
        Move-Item -LiteralPath $item.FullName -Destination (Join-Path $hold $item.Name) -Force
    } catch {
        Write-Host "Could not move $($item.Name): $($_.Exception.Message)" -ForegroundColor Yellow
    }
}

# Move FB back first.
$heldFb = Join-Path $hold 'FB'
if (Test-Path -LiteralPath $heldFb) {
    Move-Item -LiteralPath $heldFb -Destination $fbPath -Force
    Write-Host 'Moved FB back to root first.'
} else {
    Write-Host 'FB was not found in hold folder after move. Reorder may not have happened.' -ForegroundColor Yellow
}

# Put the other FB variants next, in a sensible order.
$fbOrder = @('FB64','FB128','FB16','FB20','FB20-3K','FB20-8K','FB20-MC','FB64DTV')
foreach ($name in $fbOrder) {
    $src = Join-Path $hold $name
    if (Test-Path -LiteralPath $src) {
        Move-Item -LiteralPath $src -Destination (RootPath $name) -Force
        Write-Host "Moved $name back near the top."
    }
}

# Any remaining FB* variants not in the expected list.
Get-ChildItem -LiteralPath $hold -Force -File -ErrorAction SilentlyContinue | Where-Object { $_.Name -match '^FB' } | Sort-Object Name | ForEach-Object {
    Move-Item -LiteralPath $_.FullName -Destination (RootPath $_.Name) -Force
    Write-Host "Moved $($_.Name) back near the top."
}

# Move clean C64-visible folders back before PCFILES.
$dirOrder = @('DEMOS','MUSIC','DISKMAG','PUBLIC','TOOLS','GAMES','GAMETEST','BASIC','PCFILES')
foreach ($name in $dirOrder) {
    $src = Join-Path $hold $name
    if (Test-Path -LiteralPath $src) {
        Move-Item -LiteralPath $src -Destination (RootPath $name) -Force
        Write-Host "Restored folder $name."
    }
}

# Restore root text files after folders so they do not interfere with LOAD"*",8.
foreach ($name in $knownFiles) {
    $src = Join-Path $hold $name
    if (Test-Path -LiteralPath $src) {
        Move-Item -LiteralPath $src -Destination (RootPath $name) -Force
    }
}

# Restore anything else left in hold, then remove hold.
Get-ChildItem -LiteralPath $hold -Force -ErrorAction SilentlyContinue | Sort-Object PSIsContainer, Name | ForEach-Object {
    try { Move-Item -LiteralPath $_.FullName -Destination (RootPath $_.Name) -Force }
    catch { Write-Host "Could not restore $($_.Name): $($_.Exception.Message)" -ForegroundColor Yellow }
}
try { Remove-Item -LiteralPath $hold -Force -Recurse -ErrorAction SilentlyContinue } catch {}

Write-Host ""
Write-Host 'Boot-first finalizer complete.'
Write-Host 'On the C64, LOAD"*",8 should now load FB.'
Write-Host 'Then type RUN.'
