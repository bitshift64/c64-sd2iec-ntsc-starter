try { [Net.ServicePointManager]::SecurityProtocol = [Net.SecurityProtocolType]::Tls12 } catch {}

function To-Abs($path) {
    if ([System.IO.Path]::IsPathRooted($path)) { return $path }
    return [System.IO.Path]::GetFullPath((Join-Path $Root $path))
}

function Ensure-Dir($path) {
    if (!$path) { return }
    $abs = To-Abs $path
    if (!(Test-Path -LiteralPath $abs)) { New-Item -ItemType Directory -Force -Path $abs | Out-Null }
}

function Find-7Zip {
    $candidates = @("$env:ProgramFiles\7-Zip\7z.exe", "${env:ProgramFiles(x86)}\7-Zip\7z.exe", "7z.exe")
    foreach ($c in $candidates) {
        try { $cmd = Get-Command $c -ErrorAction SilentlyContinue; if ($cmd) { return $cmd.Source } } catch {}
        if ($c -and (Test-Path -LiteralPath $c)) { return $c }
    }
    return $null
}

function Download-File($url, $out) {
    $outAbs = To-Abs $out
    Ensure-Dir (Split-Path -Parent $outAbs)
    if (Test-Path -LiteralPath $outAbs) { Write-Host "Already exists: $out"; return $true }
    Write-Host "Downloading: $url"
    Write-Host "        To: $out"
    try {
        Invoke-WebRequest -Uri $url -OutFile $outAbs -UserAgent "Mozilla/5.0 (Windows NT 10.0; Win64; x64) C64-SD2IEC-Downloader" -ErrorAction Stop
        return $true
    } catch {
        Write-Host "FAILED: $url" -ForegroundColor Yellow
        Write-Host "Reason: $($_.Exception.Message)" -ForegroundColor Yellow
        return $false
    }
}

function Expand-Any($archive, $dest) {
    $archiveAbs = To-Abs $archive
    $destAbs = To-Abs $dest
    if (!(Test-Path -LiteralPath $archiveAbs)) { Write-Host "Skipping extract. Missing: $archive" -ForegroundColor Yellow; return $false }
    Ensure-Dir $destAbs
    Write-Host "Extracting: $archive"
    Write-Host "      To: $dest"
    $seven = Find-7Zip
    if ($seven) {
        try { & $seven x $archiveAbs "-o$destAbs" -y | Out-Host; return $true }
        catch { Write-Host "7-Zip failed: $($_.Exception.Message)" -ForegroundColor Yellow }
    }
    if ($archive.ToUpperInvariant().EndsWith('.ZIP')) {
        try { Expand-Archive -LiteralPath $archiveAbs -DestinationPath $destAbs -Force -ErrorAction Stop; return $true }
        catch { Write-Host "Windows Expand-Archive failed: $($_.Exception.Message)" -ForegroundColor Yellow }
    } else {
        Write-Host "7-Zip is required for this archive type: $archive" -ForegroundColor Yellow
    }
    return $false
}

function Make-CleanName($name, $used) {
    $base = [System.IO.Path]::GetFileNameWithoutExtension($name).ToUpperInvariant()
    $ext = [System.IO.Path]::GetExtension($name).TrimStart('.').ToUpperInvariant()
    $base = $base -replace '[^A-Z0-9]', ''
    $ext = $ext -replace '[^A-Z0-9]', ''
    if (!$base) { $base = 'FILE' }
    if ($base.Length -gt 8) { $base = $base.Substring(0,8) }
    if ($ext.Length -gt 3) { $ext = $ext.Substring(0,3) }
    $candidate = if ($ext) { "$base.$ext" } else { $base }
    $i = 1
    while ($used.ContainsKey($candidate)) {
        $suffix = "$i"
        $maxBase = 8 - $suffix.Length
        if ($maxBase -lt 1) { $maxBase = 1 }
        $b = $base
        if ($b.Length -gt $maxBase) { $b = $b.Substring(0,$maxBase) }
        $candidate = if ($ext) { "$b$suffix.$ext" } else { "$b$suffix" }
        $i++
    }
    $used[$candidate] = $true
    return $candidate
}

function Copy-C64FilesClean($sourceDir, $destDir, $manifestTitle) {
    $srcAbs = To-Abs $sourceDir
    $dstAbs = To-Abs $destDir
    Ensure-Dir $dstAbs
    if (!(Test-Path -LiteralPath $srcAbs)) { Write-Host "Missing source folder: $sourceDir" -ForegroundColor Yellow; return }
    $used = @{}
    $manifest = @()
    $manifest += $manifestTitle
    $manifest += "Generated by v13. C64 visible filenames are short uppercase 8.3 style."
    $manifest += ""
    $files = Get-ChildItem -LiteralPath $srcAbs -Recurse -File -ErrorAction SilentlyContinue | Where-Object {
        $_.Extension -imatch '^\.(PRG|D64|D71|D81|SFX|SEQ|USR|LNX|LHA)$' -or $_.Name -imatch '^(D|DIRPLUS|1|FB.*)$'
    }
    foreach ($f in $files) {
        # Avoid copying source archives and obvious docs.
        if ($f.Extension -imatch '^\.(ZIP|7Z|GZ|PDF|TXT|NFO|HTM|HTML|PNG|JPG|GIF)$') { continue }
        $clean = Make-CleanName $f.Name $used
        Copy-Item -LiteralPath $f.FullName -Destination (Join-Path $dstAbs $clean) -Force
        $manifest += ("{0} <= {1}" -f $clean, $f.Name)
        Write-Host "Copied clean: $destDir\$clean"
    }
    $manifest | Out-File -Encoding ASCII (Join-Path $dstAbs "INDEX.TXT")
}

function Gunzip-File($gzPath, $outPath) {
    $gzAbs = To-Abs $gzPath
    $outAbs = To-Abs $outPath
    if (!(Test-Path -LiteralPath $gzAbs)) { return $false }
    if ((Test-Path -LiteralPath $outAbs) -and ((Get-Item -LiteralPath $outAbs).Length -gt 0)) {
        Write-Host "Already gunzipped: $outPath"
        return $true
    }
    Ensure-Dir (Split-Path -Parent $outAbs)
    try {
        if (Test-Path -LiteralPath $outAbs) { Remove-Item -LiteralPath $outAbs -Force -ErrorAction SilentlyContinue }
        $input = [System.IO.File]::Open($gzAbs, [System.IO.FileMode]::Open, [System.IO.FileAccess]::Read, [System.IO.FileShare]::Read)
        try {
            $output = [System.IO.File]::Open($outAbs, [System.IO.FileMode]::Create, [System.IO.FileAccess]::Write, [System.IO.FileShare]::None)
            try {
                $gzip = New-Object System.IO.Compression.GzipStream($input, [System.IO.Compression.CompressionMode]::Decompress)
                try { $gzip.CopyTo($output) } finally { $gzip.Dispose() }
            } finally { $output.Dispose() }
        } finally { $input.Dispose() }
        if ((Test-Path -LiteralPath $outAbs) -and ((Get-Item -LiteralPath $outAbs).Length -gt 0)) { Write-Host "Unzipped gzip: $outPath"; return $true }
    } catch {
        Write-Host "PowerShell could not gunzip: $gzPath" -ForegroundColor Yellow
        Write-Host "Reason: $($_.Exception.Message)" -ForegroundColor Yellow
    }
    $seven = Find-7Zip
    if ($seven) {
        try {
            $destDirAbs = Split-Path -Parent $outAbs
            & $seven e $gzAbs "-o$destDirAbs" -y | Out-Host
            # Rename 7-Zip output to expected name if needed.
            if (!(Test-Path -LiteralPath $outAbs)) {
                $baseNoGz = [System.IO.Path]::GetFileNameWithoutExtension($gzAbs)
                $candidate = Join-Path $destDirAbs $baseNoGz
                if (Test-Path -LiteralPath $candidate) { Move-Item -LiteralPath $candidate -Destination $outAbs -Force }
            }
            if ((Test-Path -LiteralPath $outAbs) -and ((Get-Item -LiteralPath $outAbs).Length -gt 0)) { Write-Host "7-Zip gunzipped: $outPath"; return $true }
        } catch { Write-Host "7-Zip gunzip failed: $($_.Exception.Message)" -ForegroundColor Yellow }
    }
    return $false
}

function Copy-FileBrowserVariantsToRoot {
    $fbRoot = To-Abs "PCFILES\CACHE\CBMFB"
    if (!(Test-Path -LiteralPath $fbRoot)) { Write-Host "CBM FileBrowser extract folder not found." -ForegroundColor Yellow; return }
    $candidates = Get-ChildItem -LiteralPath $fbRoot -Recurse -File -ErrorAction SilentlyContinue | Where-Object {
        $_.Name -imatch '^FB' -and $_.Name -notmatch '\.(TXT|PDF|PNG|JPG|JPEG|GIF|URL|MD|HTML?|ZIP|7Z|GZ|LHA|LNX)$'
    } | Sort-Object Name, FullName
    $copied = @{}
    foreach ($file in $candidates) {
        $destName = $file.Name.ToUpperInvariant()
        if ($destName.EndsWith('.PRG')) { $destName = $destName.Substring(0, $destName.Length - 4) }
        # Keep FB variant names mostly intact because the FB loader can depend on them.
        $destName = $destName -replace '[^A-Z0-9_+\-\.]', ''
        if ($copied.ContainsKey($destName)) { continue }
        Copy-Item -LiteralPath $file.FullName -Destination (To-Abs $destName) -Force
        $copied[$destName] = $true
        Write-Host "Copied FileBrowser variant $($file.Name) to root as $destName"
    }
    $list = ($copied.Keys | Sort-Object) -join ', '
    Write-Host "CBM FileBrowser variants copied to root: $list"
    Write-Host 'On most C64 setups use LOAD"FB",8 then RUN.'
}

